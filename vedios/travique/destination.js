const params = new URLSearchParams(window.location.search);
const country = params.get("country");
const data = countries[country];

function safe(val, fallback) {
  return val || fallback;
}

document.getElementById("country-name").textContent = data.name;
document.getElementById("country-tagline").textContent = data.tagline;
document.getElementById("capital").textContent = data.capital;
document.getElementById("currency").textContent = data.currency;
document.getElementById("season").textContent = data.season;

document.querySelector(".hero").style.backgroundImage =
  `url(${safe(data.image)})`;

const placesContainer = document.getElementById("places-container");

data.places.forEach(place => {
  placesContainer.innerHTML += `
    <div class="place-card">
      <img src="${safe(place.image)}"
      onerror="this.src='https://images.unsplash.com/photo-1500530855697-b586d89ba3ee?auto=format&fit=crop&w=1400&q=80'">

      <h3>${place.name}</h3>
      <p>${place.description}</p>
    </div>
  `;
});

document.getElementById("culture-image").src =
  safe(data.cultureImage);

document.getElementById("culture-description").textContent =
  data.culture;